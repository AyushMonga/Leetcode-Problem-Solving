class Solution {
public:
    int minSubArrayLen(int target, vector<int>& nums) {
        int i = 0;
        int min_len = nums.size() + 1; // Initialize to a value larger than possible
        int sum = 0;
        
        for (int j = 0; j < nums.size(); ++j) {
            sum += nums[j];
            while (sum >= target) {
                min_len = min(min_len, j - i + 1);
                sum -= nums[i];
                i++;
            }
        }
        
        return min_len <= nums.size() ? min_len : 0;
    }
};

















































































































































































































